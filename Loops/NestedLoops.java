package Loops;

public class NestedLoops {
    public static void main(String[] args) {
        System.out.println("Example of Nested Loop--> ");
        for(int i=1;i<=5;i++)
        {
            for(int j=1;j<=7;j++)
            {
                System.out.print("("+i+","+j+") ");

            }
            System.out.println("");
        }
    }
}
